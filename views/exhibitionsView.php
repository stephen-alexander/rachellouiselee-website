	  <h4><i>dundee print festival - 'first proof printmakers' </i></h4>
	  <br>
	  <div class="socialmedia">
	  <a href="https://twitter.com/printfestscot" target="_blank"><img src="../images/social media/TwitterLogo.png" /></a>
	  <a href="http://www.dca.org.uk/whats-on/event/print-festival-scotland" target="_blank"><img src="../images/exhibitions/2015/First Proof Printmakers/logo.jpg" /></a>
	  </div>
	  </br>
	  <p>
	  Print Festival Scotland will showcase a range of talented printmakers who have just graduated from Duncan of Jordanstone College of Art and Design. In the Cooper Gallery Project Space of DJCAD, twenty-one artists picked to represent all areas of printmaking will exhibit works that show off the level of professionalism and strength of diversity. These artist’s are: Mhairi Anderson, Abi Baikie, Lorie Ballage, Gerad Chung, Kirsty Dalton, Katy Davies, Catherine Dickson, Claire Docherty, Lynsey Ferguson, Allyson J Fraser, Rhianwen Galbraith, Lydia Hiorns, Rachel Lee, Tanith Marron, Lauren Millar, Anna Olafsson, Corey Reid, Bobby Sinclair, Amanda Speedie, Struan Teague and Guy Titterington. You will be astounded at the incredible talent on show, ranging from intricate etchings to multiple layered screen prints contrasting in colour, shape and form.
