	  <h4><i>'skin is not a screen'</i></h4>			  
	  <h4><i>djcad</i></h4> 
	  <br>
	  <p>
	  'Skin is not a Screen' is a theme demonstrating the body inside-out.</br></br>
	  Through the use of mixed media, textures and layering the complexity of the inner body and psyche is revealed. </br></br>
	  Our collaborative, small scale works have been produced to connect the anatomical function of the body with the imagination, both of which are enclosed within the body's shell.</br></br>
	  Lynne McBride</br>
	  Rachel Lee
	  </p>
